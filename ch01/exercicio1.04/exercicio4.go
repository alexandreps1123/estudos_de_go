//DESCRICAO: modificar dup2 para que exiba os nomes dos arquivos em que cada
//linha duplicada ocorre
package main

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	counts := make(map[string]int)
	files := os.Args[1:]
	if len(files) == 0 {
		countLines(os.Stdin, counts)
	} else {
		for _, arg := range files {
			f, err := os.Open(arg)
			if err != nil {
				fmt.Fprintf(os.Stderr, "dup2: %v\n", err)
				continue
			}
			countLines(f, counts)
			f.Close()
		}
	}
	for line, n := range counts {
		if n > 1 {
			//printf de 'files' exibe o nome do arquivo
			fmt.Printf("%v\t", files)
			fmt.Printf("%d\t%s\n", n, line)
		}
	}
}

func countLines(f *os.File, counts map[string]int) {
	input := bufio.NewScanner(f)
	for input.Scan() {
		counts[input.Text()]++
		if input.Text() == "" {
			break
		}
	}
	//NOTA: ignorando erros em potencial de input.Err()
}
